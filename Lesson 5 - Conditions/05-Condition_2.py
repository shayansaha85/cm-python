#### ELIF concept else if / elif

marks = 24
if (marks >= 30 and marks <= 60):
      print('C')
elif marks > 60 and marks <= 70:
      print('B')
elif marks > 70 and marks <= 90:
      print('A')
elif marks > 90 : 
      print('A+')
else:
      print('Belt se pito')